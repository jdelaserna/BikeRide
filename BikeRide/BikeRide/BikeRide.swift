import Foundation

class BikeRide {
    
    var title: String
    var map: String
    var people: String
    var rating: String
    var level: String
    
    init(title: String, map: String, people: String, rating: String, level: String) {
        self.title = title
        self.map = map
        self.people = people
        self.rating = rating
        self.level = level
    }
}
